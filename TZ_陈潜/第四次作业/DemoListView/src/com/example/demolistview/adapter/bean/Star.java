package com.example.demolistview.adapter.bean;

public class Star {
	private String name;
	private String sex;
	private int    head;
	private String face;
	private String hoobby;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public int getHead() {
		return head;
	}
	public void setHead(int head) {
		this.head = head;
	}
	public String getFace() {
		return face;
	}
	public void setFace(String face) {
		this.face = face;
	}
	public String getHoobby() {
		return hoobby;
	}
	public void setHoobby(String hoobby) {
		this.hoobby = hoobby;
	}
}
